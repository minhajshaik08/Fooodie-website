document.addEventListener("DOMContentLoaded", () => {
  const cards = document.querySelectorAll(".recipe .card");

  const modal = document.createElement("div");
  modal.id = "recipeModal";
  modal.style.display = "none";
  modal.innerHTML = `
    <div class="modal-content">
      <span class="close-button">&times;</span>
      <h2 id="recipeTitle"></h2>
      <img id="recipeImage" alt="" />
      <h3 style="color:black ;font-size: 20px; margin-top: 15px;margin-bottom: 10px">Ingredients</h3>
      <ul id="ingredientsList"></ul><br><br>
      <h3 style="color:black;font-size: 20px; margin-top: 15px;margin-bottom: 10px">Steps</h3>
      <ol id="stepsList"></ol>
    </div>
  `;
  document.body.appendChild(modal);

  const style = document.createElement("style");
  style.innerHTML = `
    #recipeModal {
      position: fixed;
      top: 0;
      left: 0;
      width: 100%;
      height: 100%;
      display: none;
      background-color: rgba(0, 0, 0, 0.7);
      justify-content: center;
      align-items: center;
      z-index: 9999;
    }
    .modal-content {
      background: #fff;
       padding: 20px;
      max-width: 600px;
      width: 90%;
      max-height: 90%;
      overflow-y: auto;
      border-radius: 10px;
      position: relative;
    }
    .modal-content img {
      width: 100%;
      max-height: 200px;
      object-fit: cover;
      margin: 10px 0;
    }
    .close-button {
      position: absolute;
      top: 10px;
      right: 20px;
      font-size: 24px;
      cursor: pointer;
    }

  `;
  document.head.appendChild(style);

  cards.forEach((card, index) => {
    card.addEventListener("click", () => {
      const keys = Object.keys(recipes);
      const recipe = recipes[keys[index]];
      if (!recipe) return;

      document.getElementById("recipeTitle").textContent = recipe.title;
      document.getElementById("recipeImage").src = recipe.image;
      document.getElementById("recipeImage").alt = recipe.title;

      const ingredientsList = document.getElementById("ingredientsList");
      ingredientsList.innerHTML = "";
      recipe.ingredients.forEach((ingredient) => {
        const li = document.createElement("li");
        li.textContent = ingredient;
        ingredientsList.appendChild(li);
      });

      const stepsList = document.getElementById("stepsList");
      stepsList.innerHTML = "";
      recipe.steps.forEach((step) => {
        const li = document.createElement("li");
        li.textContent = step;
        stepsList.appendChild(li);
      });

      modal.style.display = "flex";
    });
  });

  modal.addEventListener("click", (e) => {
    if (e.target === modal || e.target.classList.contains("close-button")) {
      modal.style.display = "none";
    }
  });
});
